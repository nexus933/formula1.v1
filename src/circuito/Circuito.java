package circuito;

public class Circuito {
	public String nombre= null;
	
	public Circuito(String nombre){
		this.nombre= nombre;
	}

	
	
		//Getters y Setters
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
		// Fin - Getters y Setters
}
